#include "minitalk.h"

void    ft_putnbr(int n)
{
    char    c;

    if (n < 0)
    {
        write(1, "-", 1);
        n *= -1;
    }
    if (n >= 0 && n <= 9)
    {
        c = n + '0';
        write(1, &c, 1);
    }
    if (n > 9)
    {
        ft_putnbr(n / 10);
        ft_putnbr(n % 10);
    }
}

